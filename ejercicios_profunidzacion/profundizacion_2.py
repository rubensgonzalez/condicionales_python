# Ejercicios de práctica con números
'''
Enunciado:
Realice un programa que solicite el ingreso de tres números
enteros, y luego en cada caso informe si el número es par
o impar.
Para cada caso imprimir el resultado en pantalla.
'''

numero1 = int(input("Ingrese el primer numero\n"))
numero2 = int(input("Ingrese el segundo numero\n"))
numero3 = int(input("Ingres el tercer numero\n"))

if (numero1 % 2) == 0:
    print("El primer numero {} es: Par ".format(numero1))
else:
    print("El primer numero {} es: Impar".format(numero1))

if (numero2 % 2) == 0:
    print("El primer numero {} es: Par ".format(numero2))
else:
    print("El primer numero {} es: Impar".format(numero2))

if (numero3 % 2) == 0:
    print("El primer numero {} es: Par ".format(numero3))
else:
    print("El primer numero {} es: Impar".format(numero3))



